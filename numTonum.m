% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
data = get(handles.indata,'String');
if isempty(data)
    msgbox('data empty!');
    return;
end
data_oct = 0;
data_hex = 0;
data_bin = 0;
switch get(handles.menu,'Value')
    case 1
        data_oct = base2dec(data,10);
        % error : data_oct = data;
    case 2
        data_oct = base2dec(data,16);
    case 3
        data_oct = base2dec(data,2);
end
data_hex = dec2hex(data_oct);
data_bin = dec2bin(data_oct);
%disp(data_hex);
%disp(data_bin);
%data_oct = strcat('OCT:',data_oct);
%data_hex = strcat('HEX:',data_hex);
%data_bin = strcat('BIN:',data_bin);
set(handles.see2,'String',data_bin);
set(handles.see10,'String',data_oct);
set(handles.see16,'String',data_hex);
